    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        try {
            HSSFWorkbook workbook = new HSSFWorkbook();
            HSSFSheet sheet = workbook.createSheet("List Producs");

            Row rowHeading = sheet.createRow(0);
            rowHeading.createCell(0).setCellValue("id");
            rowHeading.createCell(1).setCellValue("اسم المنتج");
            rowHeading.createCell(2).setCellValue("كود المنتج");
            rowHeading.createCell(3).setCellValue("السعر");
            rowHeading.createCell(4).setCellValue("الكميه");
            rowHeading.createCell(5).setCellValue("التاريخ");
            rowHeading.createCell(6).setCellValue("الوقت");

            for (int i = 0; i < 7; i++) {
                CellStyle styleWorkHeading = workbook.createCellStyle();
                HSSFFont font = workbook.createFont();
                font.setBold(true);
                font.setFontName(HSSFFont.FONT_ARIAL);
                font.setFontHeightInPoints((short) 11);
                styleWorkHeading.setFont(font);
                styleWorkHeading.setVerticalAlignment(VerticalAlignment.CENTER);
                rowHeading.getCell(i).setCellStyle(styleWorkHeading);

            }

            try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/tailor", "root", "root");
                PreparedStatement st = con.prepareStatement("select * from product ;");

                ResultSet res = st.executeQuery();
                int row = 1;
                while (res.next()) {
                    Row r = sheet.createRow(row++);
                    Cell cellID = r.createCell(0);
                    cellID.setCellValue(res.getInt("idProduct"));

                    Cell cellName = r.createCell(1);
                    cellName.setCellValue(res.getString("name"));

                    Cell cellCode = r.createCell(2);
                    cellCode.setCellValue(res.getString("code"));

                    Cell cellPrice = r.createCell(3);
                    cellPrice.setCellValue(res.getString("price"));

                    Cell cellCount = r.createCell(4);
                    cellCount.setCellValue(res.getString("count"));

                    Cell cellDate = r.createCell(5);
                    cellDate.setCellValue(res.getString("date"));

                    Cell cellTime = r.createCell(6);
                    cellTime.setCellValue(res.getString("time"));

                };

                for (int i = 0; i < 7; i++) {
                    sheet.autoSizeColumn(i);
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            }

            // save to path
            save = new JButton("save to file");
            JFileChooser fileChooser = new JFileChooser();
            FileOutputStream fos = null;

            int retval = fileChooser.showDialog(save, null);
            if (retval == JFileChooser.APPROVE_OPTION) {
                File file = fileChooser.getSelectedFile();
                if (file == null) {
                    return;
                }

                if (!file.getName().toLowerCase().endsWith(".xls")) {
                    String currentDate = formatDate(new Date());  
                    file = new File(file.getParentFile(), file.getName() + "_" + currentDate  + ".xls");
                }
                try {
                    fos = new FileOutputStream(file);
                    workbook.write(fos);
                    Desktop.getDesktop().open(file);
                } catch (Exception e) {
                    e.printStackTrace();
                } finally {
                    fos.close();
                    workbook.close();
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

